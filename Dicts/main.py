# Helper function to get the selected dictionary
def get_dictionary(language):
    if language == "Russian":
        return русскийToEng
    elif language == "German":
        return deutschToEng
    elif language == "Danish":
        return danskToEng
    elif language == "French":
        return françaisToEng
    elif language == "Spanish":
        return españolToEng
    elif language == "Mixed":
        return mixedToEng
    else:
        return None

# Main quiz function
def start_quiz(language):
    vocab = get_dictionary(language)
    if not vocab:
        messagebox.showerror("Error", "Invalid language selected!")
        return

    keys = random.sample(list(vocab.keys()), 10)
    score = 0

    for key in keys:
        
        answer = simpledialog.askstring("Quiz", f"What does this word mean in English? {key}:")
        if answer.lower() == vocab[key].lower():
            messagebox.showinfo("Result", "Correct!")
            score += 1
        else:
            messagebox.showinfo("Result", f"Incorrect. The correct answer is: {vocab[key]}")

    messagebox.showinfo("Quiz Complete", f"Your score is {score}/10")

# Setting up the GUI
root = tk.Tk()
